; MACROS ------------------------------------------------------------------

; TYPES -------------------------------------------------------------------

SEGMENT .data

ftolData		dd	0

; CODE --------------------------------------------------------------------

SEGMENT .text

;==========================================================================
;
; ftol
;
; Replacement for Visual C++ ftol.  Doesn't muck with the fpu control word
; or any of that other time consuming stuff.
;==========================================================================

global ftol
ftol:
	
	fistp qword [ftolData]
	mov eax, dword [ftolData]	
	mov edx, dword [ftolData+4]

	ret

END
